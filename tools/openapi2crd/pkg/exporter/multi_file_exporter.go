package exporter

import (
	"fmt"
	"path/filepath"

	"github.com/pkg/errors"
	"github.com/spf13/afero"
	"k8s.io/apiextensions-apiserver/pkg/apis/apiextensions"
)

// MultiFileExporter writes each CRD into its own file inside a directory.
// Files are named <crd.Name>.yaml (e.g. groups.atlas.generated.mongodb.com.yaml).
type MultiFileExporter struct {
	fs        afero.Fs
	dir       string
	overwrite bool
}

func NewMultiFileExporter(fs afero.Fs, dir string, overwrite bool) *MultiFileExporter {
	return &MultiFileExporter{
		fs:        fs,
		dir:       dir,
		overwrite: overwrite,
	}
}

func (e *MultiFileExporter) Open() error {
	exists, err := afero.DirExists(e.fs, e.dir)
	if err != nil {
		return fmt.Errorf("failed to check if directory exists: %w", err)
	}

	if !exists {
		if err = e.fs.MkdirAll(e.dir, defaultDirectoryPermission); err != nil {
			return fmt.Errorf("failed to create directory %s: %w", e.dir, err)
		}
	}

	return nil
}

func (e *MultiFileExporter) Export(crd *apiextensions.CustomResourceDefinition) error {
	yamlBytes, err := marshalCrd(crd)
	if err != nil {
		return errors.New("Failed to marshall CRD when writing output")
	}

	filename := filepath.Join(e.dir, crd.Name+".yaml")
	file, err := e.fs.OpenFile(filename, createOrUpdate, defaultFilePermission)
	if err != nil {
		return fmt.Errorf("failed to open file %s: %w", filename, err)
	}
	defer file.Close()

	if _, err = file.WriteString("# The file is generated by openapi2crd\n"); err != nil {
		return fmt.Errorf("failed to write header to file %s: %w", filename, err)
	}
	if _, err = file.Write(yamlBytes); err != nil {
		return fmt.Errorf("failed to write content to file %s: %w", filename, err)
	}

	return nil
}

func (e *MultiFileExporter) Close() error {
	return nil
}
